// Get random phrase questions (identify the word from context)
export const getRandomPhraseQuestions = (count = 10) => {
  const shuffled = [...technicalPhrases].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map(phrase => {
    // Find the corresponding vocabulary word
    const vocabWord = vocabularyWords.find(v => 
      v.word.toLowerCase() === phrase.keyword.toLowerCase()
    );
    
    // Get 3 random wrong answers from vocabulary
    const wrongAnswers = vocabularyWords
      .filter(v => v.word.toLowerCase() !== phrase.keyword.toLowerCase())
      .sort(() => Math.random() - 0.5)
      .slice(0, 3)
      .map(v => v.word);
    
    const options = [vocabWord?.word || phrase.keyword, ...wrongAnswers].sort(() => Math.random() - 0.5);
    const correctIndex = options.indexOf(vocabWord?.word || phrase.keyword);
    
    return {
      id: phrase.id,
      phrase: phrase.phrase,
      word: phrase.keyword,
      options,
      correctIndex
    };
  });
};

// Shuffle options and return new correct index
const shuffleOptions = (question) => {
  const correctAnswer = question.options[question.correctIndex];
  const shuffledOptions = [...question.options].sort(() => Math.random() - 0.5);
  const newCorrectIndex = shuffledOptions.indexOf(correctAnswer);
  return {
    options: shuffledOptions,
    correctIndex: newCorrectIndex
  };
};
