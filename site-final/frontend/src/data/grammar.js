// Get random grammar questions for quiz
export const getRandomGrammarQuestions = (count = 10) => {
  const shuffled = [...grammarQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, grammarQuestions.length)).map(q => ({
    ...q,
    ...shuffleOptions(q)
  }));
};

// Shuffle options and return new correct index
const shuffleOptions = (question) => {
  const correctAnswer = question.options[question.correctIndex];
  const shuffledOptions = [...question.options].sort(() => Math.random() - 0.5);
  const newCorrectIndex = shuffledOptions.indexOf(correctAnswer);
  return {
    options: shuffledOptions,
    correctIndex: newCorrectIndex
  };
};
